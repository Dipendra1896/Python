# Arithmetic operators
a = 10
b = 5
c= a+b
print(c)
print(a-b)
print(a*b)
print(a/b)

#Assignment operators
a = 10
a += 5
print("Now the values of a is:", a)
d = 15
d -= 5
print("Now the values of d is:", d)
e = 20
e *= 5
print("Now the values of e is:", e)
f = 25
f /= 5
print("Now the values of f is:", f)

#Comparison operators
a = 10<15
print(a)
b = 10>15
print(b)
c = 10 == 15
print(c)
d = 16<=15
print(d)

#Logical operators
a = True
